const selfClosingTags = new Set([
  "AREA",
  "BASE",
  "BR",
  "COL",
  "EMBED",
  "HR",
  "IMG",
  "INPUT",
  "LINK",
  "META",
  "PARAM",
  "SOURCE",
  "TRACK",
  "WBR",
]);

function createHtmlTag(tagName) {
  if (selfClosingTags.has(tagName)) {
    return `<${tagName.toLowerCase()} />`;
  } else {
    return `<${tagName.toLowerCase()}></${tagName.toLowerCase()}>`;
  }
}

